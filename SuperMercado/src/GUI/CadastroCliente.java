    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTableCliente.getSelectedRow() != -1) {
            tablemodelCliente.removeRow(jTableCliente.getSelectedRow());
            JOptionPane.showMessageDialog(this, "Produto removido com sucesso.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTableCliente.getSelectedRow() != -1) {
            tablemodelCliente.setValueAt(jTextFieldNomeCliente.getText(), jTableCliente.getSelectedRow(), 0);
            tablemodelCliente.setValueAt(jTextFieldCPFCliente.getText(), jTableCliente.getSelectedRow(), 1);
            tablemodelCliente.setValueAt(jTextFieldEndCliente.getText(), jTableCliente.getSelectedRow(), 2);
            tablemodelCliente.setValueAt(jTextFieldTelCliente.getText(), jTableCliente.getSelectedRow(), 3);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

